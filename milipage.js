function maininfopage() {
				document.getElementById ("deskinfopage").style.display ="flex";
				document.getElementById ("closeinfopage").style.display ="inline";
				document.getElementById ("maininfopage").style.display ="none";
				}
function closeinfopage() {
			  document.getElementById ("deskinfopage").style.display ="none";
			  document.getElementById ("closeinfopage").style.display ="none";
			  document.getElementById ("maininfopage").style.display ="inline";
				}
