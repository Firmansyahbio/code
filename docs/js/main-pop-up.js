function openView() {
  document.getElementById ("main-menu").style.display ="flex";
  document.getElementById ("closeView").style.display ="inline";
  document.getElementById ("openView").style.display ="none";
}
				
function closeView() {
  document.getElementById ("main-menu").style.display ="none";
  document.getElementById ("closeView").style.display ="none";
  document.getElementById ("openView").style.display ="inline";
}
